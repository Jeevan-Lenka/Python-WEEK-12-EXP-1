import tkinter as tk
def show(text):
	label.config(text=text)
root = tk.Tk()
root.title("Menu Demo")
menubar = tk.Menu(root)
file_menu = tk.Menu(menubar, tearoff=0)
file_menu.add_command(label="New", command=lambda: show("New"))
file_menu.add_command(label="Exit", command=root.quit)
menubar.add_cascade(label="File", menu=file_menu)
root.config(menu=menubar)
menubtn = tk.Menubutton(root, text="Options")
menubtn.pack()
menu = tk.Menu(menubtn, tearoff=0)
menu.add_command(label="Option 1", command=lambda: show("Option 1"))
menubtn.config(menu=menu)
label = tk.Label(root, text="")
label.pack()
root.mainloop()
